#include <stdio.h>

/*
   Programa: Exemplo de Comentários
   Autor: Desenvolvedor C
   Descrição: Demonstra o uso de comentários de 
   múltiplas linhas e de linha única.
*/

int main() {
    // Declarando variáveis para o cálculo
    int numero1 = 10;
    int numero2 = 20;
    int soma;

    // Realizando a soma dos dois números
    soma = numero1 + numero2;

    /* A função printf vai exibir o resultado 
       formatado na tela do usuário.
    */
    printf("A soma de %d e %d e igual a: %d\n", numero1, numero2, soma);

    return 0; // Retorna 0 para indicar que o programa terminou com sucesso
}
